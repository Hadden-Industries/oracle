CREATE OR REPLACE
FUNCTION TO_NUMBER_SAFE(gText IN VARCHAR2)
RETURN NUMBER
DETERMINISTIC PARALLEL_ENABLE
AS
    
    PRAGMA UDF;
    
BEGIN
    
    RETURN TO_NUMBER(gText);
    
EXCEPTION
WHEN VALUE_ERROR THEN
    
    RETURN NULL;
    
END;
/

