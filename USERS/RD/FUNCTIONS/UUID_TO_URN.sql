CREATE OR REPLACE
FUNCTION UUID_TO_URN(UUID RAW)
RETURN VARCHAR2
DETERMINISTIC PARALLEL_ENABLE
AS

    PRAGMA UDF;

BEGIN

    RETURN 'urn:uuid:'|| CANONICALISE_UUID(UUID);

END;
